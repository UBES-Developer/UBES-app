-- Create a table for event rsvps
create table event_rsvps (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  event_id bigint references events on delete cascade not null,
  user_id uuid references auth.users on delete cascade not null,
  unique(event_id, user_id)
);

alter table event_rsvps enable row level security;

create policy "Users can view their own RSVPs." on event_rsvps
  for select using ((select auth.uid()) = user_id);

create policy "Authenticated users can RSVP." on event_rsvps
  for insert with check ((select auth.uid()) = user_id);

create policy "Users can cancel their own RSVP." on event_rsvps
  for delete using ((select auth.uid()) = user_id);
